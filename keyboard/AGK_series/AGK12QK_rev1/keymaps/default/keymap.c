bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case KC_SWAP:
      if (!record->event.pressed) {
        return false;
      }

      switch (2 * layer_state_is(_F_KEYS) + layer_state_is(_OTHER_KEYS)) {
      case 0:
	layer_on(_F_KEYS);
	layer_on(_OTHER_KEYS);
	break;
      case 3:
	layer_off(_OTHER_KEYS);
	break;
      case 2:
	layer_off(_F_KEYS);
	break;
      case 1:
	/* This should never happen. */
	layer_off(_OTHER_KEYS);
	break;
      }
      return false;

    // Process other keycodes normally
    default:
      return true;
  }
}
